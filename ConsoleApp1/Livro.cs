class Livro
{
    public string titulo;
    public string autor;
    public string genero;
    public int anoDePublicacao;

    public Livro (string titulo, string autor, string genero, int anoDePublicacao)
    {
        this.titulo = titulo;
        this.autor = autor;
        this.genero = genero;
        this.anoDePublicacao = anoDePublicacao;
    }
}